class Solution:
    def _count_length_of_ones(self, lst_nums):
        # workaround for empty 'sums' list
        sums = [0]

        curr_sum = 0
        if 0 not in lst_nums:
            return sum(lst_nums)

        for n in lst_nums:
            if n == 1:
                curr_sum += 1
            else:
                if curr_sum > 0:
                    sums.append(curr_sum)
                    curr_sum = 0

        # append to sums the last curr_sum value (for cases when lst_nums ends with 1)
        if curr_sum > 0:
            sums.append(curr_sum)
        return max(sums)

    def longest_ones(self, nums, k):
        # if the 'nums' list will remain the same just proceed to calculation of 1s
        if k == 0:
            return self._count_length_of_ones(nums)

        ones_max_val = 0
        iterations = len(nums)

        for i in range(iterations):
            # break if exceeding ones max value is impossible
            if ones_max_val >= iterations - i:
                break

            # we must keep k (amount of 0 to 1 reversions) value unchanged
            reduce = k

            # create a cut-down list based on current value of i
            tmp_lst = nums[i:]

            # revert 0s to 1s in a temp list, limit is the value of 'reduce'
            for j in range(len(tmp_lst)):
                if 0 not in tmp_lst or reduce == 0:
                    break
                if tmp_lst[j] == 0 and reduce > 0:
                    tmp_lst[j] = 1
                    reduce -= 1

            # calculate 1s in the temp list
            amount_ones = self._count_length_of_ones(tmp_lst)

            # save maximum amount of 1s to a variable
            if amount_ones > ones_max_val:
                ones_max_val = amount_ones
        return ones_max_val


